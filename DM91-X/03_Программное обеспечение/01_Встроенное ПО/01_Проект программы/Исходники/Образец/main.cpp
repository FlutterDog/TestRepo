#include "app/project.hpp"
#include "app/app.hpp"

#include <avr/interrupt.h>

/**
 * @file main.cpp
 * @brief Точка входа встроенного программного обеспечения LAI.
 *
 * @details
 * Файл содержит минимальную последовательность запуска приложения:
 * - запрет прерываний на время базовой инициализации;
 * - запуск системного миллисекундного тикера;
 * - разрешение прерываний;
 * - выполнение прикладной инициализации setup();
 * - непрерывное выполнение основного цикла loop().
 */

/**
 * @brief Главная функция программы.
 *
 * @return Функция не возвращает управление.
 */
int main(void)
{
    cli();

    hal::tick_init();

    sei();

    setup();

    for (;;)
    {
        loop();
    }
}