#include "compat.hpp"

#include <avr/io.h>
#include <stdint.h>

/**
 * @file compat_spi.cpp
 * @brief Базовый SPI master-драйвер для ATmega328P.
 *
 * @details
 * Модуль предоставляет минимальный синхронный обмен по аппаратному SPI.
 * В текущей прошивке DM-91x основной измерительный цикл не использует SPI,
 * однако файл может оставаться в составе общего HAL для единообразия
 * структуры проекта.
 *
 * Настройки интерфейса:
 * - режим ведущего;
 * - SPI mode 0;
 * - передача старшим битом вперёд;
 * - частота SCK = F_CPU / 16.
 *
 * @note
 * Функции модуля не управляют линиями выбора внешних устройств.
 * Управление CS/SS должно выполняться на уровне прикладного или board-слоя.
 */

namespace hal
{
    /**
     * @brief Инициализирует аппаратный SPI в режиме master.
     *
     * @details
     * Для ATmega328P настраиваются стандартные линии аппаратного SPI:
     * - PB2 / SS — выход, удерживается в высоком уровне;
     * - PB3 / MOSI — выход;
     * - PB4 / MISO — вход;
     * - PB5 / SCK — выход.
     *
     * PB2 должен быть настроен как выход, чтобы контроллер оставался
     * в режиме ведущего устройства.
     */
    void spi_init(void)
    {
        DDRB |= static_cast<uint8_t>(_BV(PB2) | _BV(PB3) | _BV(PB5));
        DDRB &= static_cast<uint8_t>(~_BV(PB4));

        PORTB |= static_cast<uint8_t>(_BV(PB2));

        SPCR = static_cast<uint8_t>(_BV(SPE) | _BV(MSTR) | _BV(SPR0));
        SPSR = 0U;
    }

    /**
     * @brief Передаёт один байт по SPI и возвращает одновременно принятый байт.
     *
     * @param value Байт для передачи.
     *
     * @return Байт, принятый во время обмена.
     *
     * @details
     * Функция выполняет блокирующее ожидание завершения передачи байта.
     * Вызывающая сторона должна заранее выбрать нужное внешнее устройство
     * линией CS/SS и снять выбор после завершения обмена.
     */
    uint8_t spi_txrx(uint8_t value)
    {
        SPDR = value;

        while ((SPSR & _BV(SPIF)) == 0U)
        {
            /*
             * Ожидание завершения передачи байта.
             */
        }

        return SPDR;
    }
}
