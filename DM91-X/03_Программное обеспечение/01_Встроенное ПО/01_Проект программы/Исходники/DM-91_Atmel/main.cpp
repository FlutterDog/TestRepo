#include "app/project.hpp"
#include "app/app.hpp"

#include <avr/interrupt.h>

/**
 * @file main.cpp
 * @brief Точка входа встроенного программного обеспечения DM-91x.
 *
 * @details
 * Файл содержит минимальную последовательность запуска прошивки датчика
 * плотности DM-91x:
 * - запрет прерываний на время базовой инициализации;
 * - запуск системного миллисекундного таймера HAL;
 * - разрешение прерываний;
 * - выполнение прикладной инициализации setup();
 * - непрерывное выполнение основного прикладного цикла loop().
 *
 * Основная логика измерения, обработки данных, обмена по Modbus RTU,
 * работы с EEPROM-настройками и управления исполнением Arktic находится
 * в модуле app.
 */

/**
 * @brief Главная функция программы.
 *
 * @details
 * Функция инициализирует системный тикер, запускает прикладную часть
 * прошивки через setup() и далее постоянно вызывает loop().
 *
 * @return Функция не возвращает управление.
 */
int main(void)
{
    cli();

    hal::tick_init();

    sei();

    setup();

    for (;;)
    {
        loop();
    }
}