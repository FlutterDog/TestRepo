#ifndef SNB_DEFINITIONS_H_
#define SNB_DEFINITIONS_H_

// Стандартные типы данных в сокращённой нотации:
typedef uint8_t		byte;
typedef uint8_t		u8;
typedef uint16_t	u16;
typedef uint32_t	u32;
typedef int8_t		s8;
typedef int16_t		s16;
typedef int32_t		s32;

#define	ON			1
#define	OFF			0
#define	YES			1
#define	NO			0

#define	IDLE		0
#define	BUSY		1
#define	ACTIVE		1

#define	HIDE		0
#define	SHOW		1


#endif /* SNB_DEFINITIONS_H_ */