#pragma once

#include "project.hpp"

/**
 * @file app.hpp
 * @brief Публичный интерфейс прикладного уровня LDO.
 *
 * @details
 * Заголовок объявляет только две функции жизненного цикла приложения:
 * - setup() — однократная инициализация после старта микроконтроллера;
 * - loop() — основной циклический обработчик.
 *
 * Такая модель оставлена намеренно, чтобы сохранить понятную структуру
 * старого Arduino-подобного проекта, но при этом убрать зависимость
 * от Arduino core.
 *
 * Все внутренние функции приложения, работа с реле, адресным переключателем,
 * Modbus-регистрами и портами платы скрыты внутри app.cpp.
 */

/**
 * @brief Выполняет однократную инициализацию приложения LDO.
 *
 * @details
 * Функция вызывается один раз из main().
 *
 * Выполняет:
 * - настройку watchdog;
 * - настройку портов ввода-вывода;
 * - чтение адресного переключателя;
 * - инициализацию Modbus/RS-485;
 * - начальную установку программных таймеров.
 */
void setup(void);

/**
 * @brief Выполняет один проход основного цикла приложения LDO.
 *
 * @details
 * Функция вызывается бесконечно из main().
 *
 * Выполняет:
 * - обновление текущего времени;
 * - обработку периодических задач;
 * - обслуживание Modbus;
 * - сброс watchdog.
 */
void loop(void);