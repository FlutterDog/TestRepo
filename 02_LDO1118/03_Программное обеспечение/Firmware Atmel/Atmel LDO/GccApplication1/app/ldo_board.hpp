#pragma once

#include "project.hpp"

#include <avr/io.h>
#include <stdint.h>

/**
 * @file ldo_board.hpp
 * @brief Аппаратный слой платы LDO.
 *
 * @details
 * Модуль предоставляет единый интерфейс к аппаратным линиям платы:
 * - выбор аппаратной ревизии;
 * - инициализация портов ввода-вывода;
 * - чтение адресного переключателя;
 * - управление светодиодом статуса;
 * - управление направлением RS-485;
 * - управление релейными выходами.
 *
 * Прикладной код и транспорт Modbus используют функции этого модуля
 * и не работают напрямую с регистрами PORTx/DDRx для сигналов платы.
 */

namespace ldo_board
{
    /**
     * @brief Аппаратные ревизии платы LDO.
     *
     * @details
     * Ревизия определяет назначение линии управления RS-485 и карту
     * релейных выходов.
     */
    enum BoardRevision
    {
        /**
         * @brief Ревизия платы с управлением RS-485 на PB2.
         *
         * @details
         * В этой ревизии:
         * - RS-485 DE/RE подключён к PB2;
         * - релейные выходы подключены к PD2...PD7, PB0, PB1.
         */
        BOARD_OLD_RS485_PB2 = 0,

        /**
         * @brief Ревизия платы с управлением RS-485 на PD2.
         *
         * @details
         * В этой ревизии:
         * - RS-485 DE/RE подключён к PD2;
         * - релейные выходы подключены к PD3...PD7, PB0, PB1, PB2.
         */
        BOARD_NEW_RS485_PD2 = 1
    };

    /**
     * @brief Инициализирует аппаратные линии платы.
     *
     * @details
     * Настраивает:
     * - светодиод статуса;
     * - входы адресного переключателя;
     * - линию управления RS-485;
     * - релейные выходы.
     *
     * После инициализации релейные выходы сбрасываются в ноль,
     * а RS-485 переводится в режим приёма.
     */
    void initIo(void);

    /**
     * @brief Считывает адрес устройства с аппаратного адресного переключателя.
     *
     * @return Modbus-адрес устройства.
     *
     * @details
     * Если все биты адресного переключателя равны нулю, возвращается адрес 1.
     */
    uint8_t readSlaveAddress(void);

    /**
     * @brief Устанавливает состояние светодиода статуса.
     *
     * @param state true — включить светодиод; false — выключить светодиод.
     */
    void statusLedWrite(bool state);

    /**
     * @brief Инвертирует состояние светодиода статуса.
     */
    void statusLedToggle(void);

    /**
     * @brief Переводит RS-485-трансивер в режим передачи.
     *
     * @details
     * Устанавливает активный уровень на линии DE/RE выбранной ревизии платы.
     */
    void rs485TransmitMode(void);

    /**
     * @brief Переводит RS-485-трансивер в режим приёма.
     *
     * @details
     * Сбрасывает активный уровень на линии DE/RE выбранной ревизии платы.
     */
    void rs485ReceiveMode(void);

    /**
     * @brief Проверяет текущее состояние линии управления RS-485.
     *
     * @return true — трансивер находится в режиме передачи.
     * @return false — трансивер находится в режиме приёма.
     */
    bool rs485IsTransmitMode(void);

    /**
     * @brief Записывает состояние релейных выходов.
     *
     * @param state Битовое состояние релейных выходов.
     *
     * @details
     * Соответствие битов физическим линиям зависит от выбранной ревизии платы.
     */
    void relaysWrite(uint8_t state);
}