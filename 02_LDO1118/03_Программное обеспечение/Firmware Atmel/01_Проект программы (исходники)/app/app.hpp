/**
 * @file app.hpp
 * @brief Публичный интерфейс прикладного уровня прошивки (Application Layer).
 *
 * @details
 * Заголовок объявляет функции и внешние символы, через которые прикладной модуль
 * взаимодействует с остальными частями прошивки (инициализация, цикл, обработка
 * измерений, калибровки, сервисные процедуры).
 *
 * Реализации см. в app.cpp и связанных модулях проекта.
 */

#pragma once

#include <stdint.h>
#include "project.hpp"         // Базовые типы/настройки проекта (в т.ч. byte при наличии)
#include "../Libs/ADC_init.h"  // Инициализация/драйвер АЦП (зависимость проекта)

/**
 * @typedef AppPtr_t
 * @brief Тип указателя на точку входа (переход в приложение/бутлоадер).
 */
typedef void (*AppPtr_t)(void);

/**
 * @brief Указатель на текущую функцию коммуникации.
 * @details
 * Диспетчеризация режима связи (например, Slave/альтернативный режим).
 * Устанавливается, как правило, в initCommunication() и/или сервисных процедурах.
 */
extern void (*commFunc)(void);

/**
 * @brief Адрес перехода в бутлоадер (кастомный Optiboot).
 * @details
 * Значение задаётся в app.cpp и зависит от сборки/линковки бутлоадера.
 * При изменении размера/разметки секций бутлоадера адрес должен быть пересчитан.
 */
extern AppPtr_t GoToBootloader;

// =============================================================================
// Жизненный цикл приложения
// =============================================================================

/**
 * @brief Первичная инициализация оборудования и сервисов прошивки.
 */
void setup(void);

/**
 * @brief Основной цикл выполнения (главная кооперативная петля).
 */
void loop(void);

// =============================================================================
// Инициализация / сервис
// =============================================================================

/**
 * @brief Инициализация линий ввода-вывода и управляющих сигналов.
 */
void initPinOut(void);

/**
 * @brief Обработчик прерывания таймера: сервис дисплея (кламп CS по SPI).
 * @note Реализация должна быть максимально короткой (ISR-контекст).
 */
void clampDisplay(void);

/**
 * @brief Загрузка настроек и калибровочных параметров из EEPROM.
 * @details При отсутствии валидного флага выполняет defaultSettings().
 */
void loadSettings(void);

/**
 * @brief Применение пользовательских/калибровочных настроек (tuned settings).
 */
void tunedSettings(void);

/**
 * @brief Инициализация настроек по умолчанию и их сохранение (где требуется).
 * @warning Использовать ограниченно из-за ресурса EEPROM.
 */
void defaultSettings(void);

/**
 * @brief Формирование стартовых сообщений/индикаций.
 */
void startMessages(void);

/**
 * @brief Инициализация коммуникационных интерфейсов (Modbus/RS-485 и др.).
 */
void initCommunication(void);

/**
 * @brief Обновление системного времени (миллисекунды) в глобальном счётчике.
 */
void getTime(void);

// =============================================================================
// Главный цикл / тайминги / дисплей
// =============================================================================

/**
 * @brief Съём и первичная обработка измерений (управление окнами, семплирование).
 */
void measurements(void);

/**
 * @brief Периодические задачи с периодом 1 с.
 */
void loop_1sec(void);

/**
 * @brief Периодические задачи с шагом порядка 10 мс (по системному флагу).
 */
void loop_10ms(void);

/**
 * @brief Обновление индикации (передача буфера на дисплей при готовности).
 */
void displayProceed(void);

/**
 * @brief Системные таймеры/таймауты (несколько независимых «тикеров»).
 */
void systemTimer(void);

// =============================================================================
// Математика / фильтры / обработка
// =============================================================================

/**
 * @brief Адаптивное сглаживание отображаемой концентрации.
 * @details Обновляет выходной сигнал сглаживания по текущей ошибке и задержке.
 */
void smoothDisplay(void);

/**
 * @brief Применить шаг сглаживания к выходному значению.
 * @param math_ Флаг направления: ненулевое — прибавить, 0 — вычесть.
 * @param value_ Величина шага.
 */
void smoothMath(byte math_, int16_t value_);

/**
 * @brief Снятие температур с АЦП (сырые значения).
 */
void getTemp(void);

/**
 * @brief Усреднение сигналов по окну (внутренний буфер).
 */
void averaging(void);

/**
 * @brief Нормализация усреднённых значений с использованием коэффициентов.
 */
void normalization(void);

/**
 * @brief Кольцевой буфер тренда нормированных значений.
 */
void trendBuffer(void);

/**
 * @brief Проверка трендов/истории сигналов и обновление производных/флагов.
 */
void checkTrend(void);

/**
 * @brief Адаптивная фильтрация PIDFILTER (выбор коэффициента по шуму в окне).
 */
void noiseFilterPID(void);

/**
 * @brief GAP-фильтрация: «подтягивание» GAPFILTERED к PIDFILTER при устойчивом расхождении.
 */
void noiseGAPFILTERED(void);

/**
 * @brief Обновление производных сигналов и вычисление «дельты» и температур.
 */
void newSpan(void);

/**
 * @brief Пересчёт сенсорного сигнала в концентрацию по кусочно-линейной аппроксимации.
 */
void sensorToConcentration(void);

/**
 * @brief Пошаговый конвейер обработки аналоговых данных по флагу newDataArrived.
 */
void processAnalogSignals(void);

// =============================================================================
// Калибровка / EEPROM / статусы / ошибки
// =============================================================================

/**
 * @brief Сохранить точку калибровки газа в EEPROM и пересчитать коэффициенты участка.
 * @param pointer Индекс точки (ожидаемо 1..7 по текущей реализации).
 */
void gasToEEPROM(byte pointer);

/**
 * @brief Загрузить точку калибровки газа из EEPROM и восстановить коэффициенты участка.
 * @param pointer Индекс точки (ожидаемо 1..7 по текущей реализации).
 */
void gasFromEEPROM(byte pointer);

/**
 * @brief Пересчёт X-точек кривой при изменении нулевой точки.
 * @param pointer Индекс точки 1..7 (0 — базовая точка).
 * @param oldZero_ Предыдущее значение X[0].
 */
void ZeroReTune(byte pointer, int16_t oldZero_);

/**
 * @brief Пересчёт точки (X,Y) при изменении верхнего предела (span).
 * @param pointer Индекс точки 0..6 (7 — репер верхнего предела).
 * @param oldTopLimit_X Предыдущее значение X[7].
 * @param oldTopLimit_Y Предыдущее значение Y[7].
 */
void UpperLimitReTune(byte pointer, int16_t oldTopLimit_X, int16_t oldTopLimit_Y);

/**
 * @brief Обеспечить строгую монотонность «лестницы» sensorAnalogX[].
 */
void CheckADLadder(void);

/**
 * @brief Проверка условий ошибок и формирование состояния.
 */
void checkError(void);

/**
 * @brief Пролистывание и вывод кодов ошибок на индикатор.
 */
void displayErrorMsg(void);

/**
 * @brief Установить бит ошибки в stateByte.
 * @param bitErr_ Позиция бита (0..15 по типу регистра состояния).
 */
void detectorStatus(int16_t bitErr_);

/**
 * @brief Формирование текстовых сообщений/индикаций на дисплее по приоритетам.
 */
void makeMessage(void);

/**
 * @brief Сигнал «операция выполнена»: краткий показ "done".
 */
void replyDone(void);

/**
 * @brief Калибровка нуля (ZERO).
 */
void calibrZero(void);

/**
 * @brief Калибровка верхнего предела (SPAN).
 * @param Value_ Новое значение верхней точки по Y (gasConcentrationY[7]).
 */
void calibrSpan(int16_t Value_);

// =============================================================================
// Потенциометр / низкоуровневые линии
// =============================================================================

/**
 * @brief Линия CS потенциометра: высокий уровень.
 */
void cs_high(void);

/**
 * @brief Линия CS потенциометра: низкий уровень.
 */
void cs_low(void);

/**
 * @brief Линия UD потенциометра: высокий уровень.
 */
void ud_high(void);

/**
 * @brief Линия UD потенциометра: низкий уровень.
 */
void ud_low(void);

/**
 * @brief Линия DATA (для HC-протокола): высокий уровень.
 */
void potData_high(void);

/**
 * @brief Линия DATA (для HC-протокола): низкий уровень.
 */
void potData_low(void);

/**
 * @brief Установка положения потенциометра (пошаговый, 0..63).
 * @param potTarget_ Целевое положение.
 * @return 0 — выполнено (по текущей реализации).
 */
byte setPot(uint8_t potTarget_);

/**
 * @brief Установка положения потенциометра HC (сдвиговый протокол, 8 бит).
 * @param potTarget_ Целевое значение 0..255.
 * @return 0 — выполнено (по текущей реализации).
 */
byte setHCPot(uint8_t potTarget_);

/**
 * @brief Инициализация пошагового потенциометра (приведение в ноль).
 * @return 0 — выполнено (по текущей реализации).
 */
byte initPot(void);

// =============================================================================
// Семплирование / ИК-источник / таймеры задач
// =============================================================================

/**
 * @brief Сброс таймера окна ИК-источника.
 */
void resetIRTimer(void);

/**
 * @brief Сброс таймера семплирования образцов.
 */
void resetSampleTimer(void);

/**
 * @brief Захват выборки сигнала (чтение АЦП и обновление экстремумов).
 */
void getSample(void);

/**
 * @brief Управление ИК-источником.
 * @param action Ненулевое — включить, 0 — выключить.
 */
void IRSource(byte action);

/**
 * @brief Таймер «периода» измерительного цикла.
 * @return 1 — пора запускать новый цикл; 0 — ещё нет.
 */
byte taskTimer(void);

/**
 * @brief Сброс таймера измерительного периода.
 */
void resetTaskTimer(void);

/**
 * @brief Перенос рассчитанных экстремумов/пиков в рабочие буферы.
 */
void dataToBuffer(void);

// =============================================================================
// Производные / флаги / опорные точки
// =============================================================================

/**
 * @brief Расчёт производной для выбранного канала по кольцевому буферу TREND.
 * @param signalCh_ Индекс канала.
 */
void calcDrvtive(byte signalCh_);

/**
 * @brief Машина состояний обнаружения газа/шума/термошока; формирует impactByte.
 */
void calcGasFlag(void);

/**
 * @brief Вычисление/обновление «угловых точек» (cornerSTONE) тренда.
 * @param channel_ Канал.
 * @param startPoint_ Смещение от хвоста назад.
 */
void getCornerPoints(byte channel_, byte startPoint_);

/**
 * @brief Обновить «нулевую линию» и коэффициент сдвига.
 */
void zeroDet(void);

// =============================================================================
// Нагреватель / задержки
// =============================================================================

/**
 * @brief Управление цепью нагревателя (включение/выключение).
 * @param state_ 1 — включить, 0 — выключить.
 */
void heaterBreaker(byte state_);

/**
 * @brief Запуск программной задержки (в миллисекундах).
 * @param time_ Длительность задержки, мс.
 */
void startDelay(uint16_t time_);

/**
 * @brief Проверка окончания программной задержки.
 * @return 1 — время вышло, 0 — ещё нет.
 */
byte finishDelay(void);

// =============================================================================
// Коммуникации / скорость
// =============================================================================

/**
 * @brief Режим Modbus slave: обслуживание стека.
 */
void slaveMode(void);

/**
 * @brief Выбор скорости UART по индексу предустановки.
 * @param command_ Индекс 0..6 (набор скоростей определён реализацией).
 */
void getBaud(byte command_);

/**
 * @brief Расчёт межкадровой паузы Modbus RTU (мс) по скорости baud.
 * @param baud Скорость UART (бод).
 * @return Рекомендуемая пауза между кадрами, мс.
 */
uint16_t modbus_pause_ms(uint32_t baud);
