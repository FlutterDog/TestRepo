#include "app/project.hpp"
#include "app/app.hpp"
#include <avr/interrupt.h>

/**
 * @file main.cpp
 * @brief Точка входа приложения (AVR).
 *
 * @details
 * Выполняет минимальную инициализацию платформы:
 * - запрещает прерывания на время настройки базовых модулей;
 * - инициализирует миллисекундный тикер (hal::tick_init), необходимый для millis()/delay();
 * - разрешает прерывания и передаёт управление прикладному уровню (setup()/loop()).
 *
 * Архитектурно приложение реализовано в виде бесконечного цикла `loop()`,
 * что сохраняет совместимость с кодовой базой, перенесённой из Arduino-подхода,
 * но при этом точка входа и низкоуровневая инициализация остаются явными и контролируемыми.
 */

int main(void)
{
	// На старте запрещаем прерывания: регистры таймера и общие структуры
	// должны быть приведены в согласованное состояние до первого ISR.
	cli();

	// Инициализация Timer0 для 1 кГц тика. Используется функциями millis()/delay().
	hal::tick_init();

	// После настройки базового таймера можно разрешить прерывания.
	sei();

	// Прикладная инициализация.
	setup();

	// Основной цикл приложения. Выход не предусмотрен.
	for (;;) {
		loop();
	}
}
