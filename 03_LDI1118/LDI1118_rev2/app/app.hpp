#pragma once

#include "project.hpp"

/**
 * @file app.hpp
 * @brief Публичный интерфейс прикладного уровня LDI.
 *
 * @details
 * Заголовок объявляет функции жизненного цикла приложения:
 * - setup() — однократная инициализация после старта микроконтроллера;
 * - loop() — основной циклический обработчик.
 *
 * Внутренняя логика приложения, карта Modbus-регистров, чтение дискретных
 * входов, индикация состояния, аппаратная карта платы и переход в bootloader
 * реализованы в app.cpp и связанных модулях.
 */

/**
 * @brief Выполняет однократную инициализацию приложения LDI.
 *
 * @details
 * Функция вызывается один раз из main().
 *
 * Выполняет:
 * - настройку watchdog;
 * - инициализацию аппаратных линий платы;
 * - чтение аппаратного адресного переключателя;
 * - первичное чтение дискретных входов;
 * - первичное обновление LED-индикации;
 * - инициализацию Modbus RTU по RS-485;
 * - начальную установку программных таймеров.
 */
void setup(void);

/**
 * @brief Выполняет один проход основного цикла приложения LDI.
 *
 * @details
 * Функция вызывается бесконечно из main().
 *
 * Выполняет:
 * - обновление текущего времени;
 * - чтение шестнадцати дискретных входов;
 * - обновление LED-индикации входов;
 * - обработку периодических задач;
 * - обслуживание Modbus RTU;
 * - сброс watchdog.
 */
void loop(void);