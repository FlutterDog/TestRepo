#pragma once

#include "project.hpp"

#include <avr/io.h>
#include <stdint.h>

/**
 * @file ldi_board.hpp
 * @brief Аппаратный слой платы LDI.
 *
 * @details
 * Модуль предоставляет единый интерфейс к аппаратным линиям платы LDI:
 * - инициализация портов ввода-вывода;
 * - чтение аппаратного адресного переключателя;
 * - управление светодиодом состояния связи;
 * - управление направлением RS-485;
 * - чтение шестнадцати дискретных входов через 74HC165;
 * - вывод состояния входов на LED-панель через 74HC595.
 *
 * Прикладной код и транспорт Modbus используют функции этого модуля
 * и не работают напрямую с регистрами PORTx/DDRx для сигналов платы.
 */

namespace ldi_board
{
    /**
     * @brief Количество дискретных входов модуля LDI.
     */
    static const uint8_t INPUT_COUNT = 16u;

    /**
     * @brief Инициализирует аппаратные линии платы LDI.
     *
     * @details
     * Настраивает:
     * - входы адресного переключателя;
     * - линии UART/RS-485;
     * - линии 74HC165;
     * - линии 74HC595;
     * - светодиод состояния связи.
     *
     * После инициализации:
     * - RS-485 переводится в режим приёма;
     * - LED-панель сбрасывается в ноль;
     * - светодиод состояния связи выключается.
     */
    void initIo(void);

    /**
     * @brief Считывает Modbus-адрес с аппаратного адресного переключателя.
     *
     * @return Modbus-адрес устройства.
     *
     * @details
     * Адресный переключатель подключён к PC0...PC4.
     * Входы работают с внутренними подтяжками к Vcc.
     * Активное положение переключателя даёт логический ноль на входе.
     *
     * После инверсии и зеркального разворота битов соответствие такое:
     * - PC4 -> bit 0, 0b00000001;
     * - PC3 -> bit 1, 0b00000010;
     * - PC2 -> bit 2, 0b00000100;
     * - PC1 -> bit 3, 0b00001000;
     * - PC0 -> bit 4, 0b00010000.
     *
     * Если считанное значение равно нулю, возвращается адрес 1.
     */
    uint8_t readSlaveAddress(void);

    /**
     * @brief Устанавливает состояние светодиода связи.
     *
     * @param state true — включить светодиод; false — выключить светодиод.
     */
    void statusLedWrite(bool state);

    /**
     * @brief Инвертирует состояние светодиода связи.
     */
    void statusLedToggle(void);

    /**
     * @brief Переводит RS-485-трансивер в режим передачи.
     */
    void rs485TransmitMode(void);

    /**
     * @brief Переводит RS-485-трансивер в режим приёма.
     */
    void rs485ReceiveMode(void);

    /**
     * @brief Проверяет состояние линии управления RS-485.
     *
     * @return true — трансивер находится в режиме передачи.
     * @return false — трансивер находится в режиме приёма.
     */
    bool rs485IsTransmitMode(void);

    /**
     * @brief Считывает состояние шестнадцати дискретных входов.
     *
     * @return Битовое состояние входов.
     *
     * @details
     * Чтение выполняется через последовательный выход 74HC165.
     * Первый считанный бит помещается в bit 15 результата,
     * последний считанный бит помещается в bit 0 результата.
     *
     * Логическая единица на выходе 74HC165 записывается как 1
     * в соответствующий бит результата.
     *
     * @note
     * Линии latch и clock общие для 74HC165 и 74HC595. Поэтому после чтения
     * входов аппаратный слой восстанавливает последнее состояние LED-панели.
     */
    uint16_t readInputs(void);

    /**
     * @brief Выводит состояние входов на LED-панель.
     *
     * @param state Битовое состояние входов.
     *
     * @details
     * Перед выводом на 74HC595 выполняется перестановка младшего
     * и старшего байта, соответствующая разводке LED-панели.
     */
    void writeInputLeds(uint16_t state);

    /**
     * @brief Выводит произвольную маску на LED-панель.
     *
     * @param state Битовая маска светодиодов.
     *
     * @details
     * Функция применяет ту же перестановку байтов, что и writeInputLeds().
     * Последняя переданная маска сохраняется внутри аппаратного слоя и
     * используется для восстановления LED-панели после чтения входов.
     */
    void writePanelMask(uint16_t state);
}