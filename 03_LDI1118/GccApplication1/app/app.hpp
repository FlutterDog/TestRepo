#pragma once

#include "project.hpp"

/**
 * @file app.hpp
 * @brief Публичный интерфейс прикладного уровня LDO.
 *
 * @details
 * Заголовок объявляет функции жизненного цикла приложения:
 * - setup() — однократная инициализация после старта микроконтроллера;
 * - loop() — основной циклический обработчик.
 *
 * Внутренняя логика приложения, карта Modbus-регистров, управление реле,
 * адресным переключателем, аппаратной картой платы и переходом в bootloader
 * реализованы в app.cpp и связанных модулях.
 */

/**
 * @brief Выполняет однократную инициализацию приложения LDO.
 *
 * @details
 * Функция вызывается один раз из main().
 *
 * Выполняет:
 * - настройку watchdog;
 * - инициализацию аппаратных линий платы;
 * - чтение адресного переключателя;
 * - инициализацию Modbus RTU по RS-485;
 * - начальную установку программных таймеров.
 */
void setup(void);

/**
 * @brief Выполняет один проход основного цикла приложения LDO.
 *
 * @details
 * Функция вызывается бесконечно из main().
 *
 * Выполняет:
 * - обновление текущего времени;
 * - обработку периодических задач;
 * - обслуживание Modbus RTU;
 * - сброс watchdog.
 */
void loop(void);