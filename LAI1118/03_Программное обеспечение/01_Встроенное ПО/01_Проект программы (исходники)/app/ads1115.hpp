#pragma once

#include "project.hpp"

#include <stdint.h>

/**
 * @file ads1115.hpp
 * @brief Драйвер аналого-цифрового преобразователя ADS1115.
 *
 * @details
 * Файл содержит интерфейс доступа к микросхеме ADS1115 по шине I2C.
 * Драйвер используется для запуска одиночных преобразований, проверки
 * готовности результата и чтения шестнадцатиразрядного значения АЦП.
 *
 * Поддерживаются:
 * - одиночный режим преобразования;
 * - single-ended входы AIN0...AIN3;
 * - настройка диапазона входного сигнала;
 * - настройка скорости преобразования;
 * - чтение регистра результата преобразования.
 */

/**
 * @class ADS1115
 * @brief Драйвер микросхемы ADS1115.
 *
 * @details
 * Класс хранит I2C-адрес микросхемы и параметры конфигурации,
 * используемые при запуске одиночного преобразования.
 */
class ADS1115
{
public:
    /**
     * @brief Создаёт объект драйвера ADS1115.
     *
     * @param address Семибитный I2C-адрес микросхемы.
     */
    explicit ADS1115(uint8_t address);

    /**
     * @brief Инициализирует интерфейс обмена с ADS1115.
     *
     * @details
     * Функция настраивает шину I2C на рабочую частоту, используемую
     * для обмена с микросхемами ADS1115 на плате LAI.
     *
     * @return true — инициализация выполнена.
     */
    bool begin(void);

    /**
     * @brief Устанавливает диапазон входного усилителя ADS1115.
     *
     * @details
     * Значение записывается в поле PGA конфигурационного регистра.
     * Фактическая интерпретация значения соответствует документации
     * на микросхему ADS1115.
     *
     * @param gain Код диапазона входного усилителя.
     */
    void setGain(uint8_t gain);

    /**
     * @brief Устанавливает режим работы преобразователя.
     *
     * @param mode Ненулевое значение — одиночный режим; ноль — непрерывный режим.
     */
    void setMode(uint8_t mode);

    /**
     * @brief Устанавливает скорость преобразования ADS1115.
     *
     * @details
     * Значение записывается в поле DR конфигурационного регистра.
     * Фактическая частота преобразования определяется таблицей
     * скоростей из документации ADS1115.
     *
     * @param dataRate Код скорости преобразования.
     */
    void setDataRate(uint8_t dataRate);

    /**
     * @brief Запускает одиночное преобразование выбранного канала.
     *
     * @param channel Номер single-ended канала: AIN0...AIN3.
     *
     * @return true — команда запуска передана успешно;
     *         false — номер канала некорректен или обмен по I2C завершился ошибкой.
     */
    bool startSingleShot(uint8_t channel);

    /**
     * @brief Проверяет состояние текущего преобразования.
     *
     * @return true — преобразование ещё выполняется;
     *         false — результат преобразования готов.
     */
    bool isBusy(void);

    /**
     * @brief Читает последнее значение из регистра преобразования.
     *
     * @return Шестнадцатиразрядный знаковый код АЦП.
     */
    int16_t getValue(void);

private:
    /**
     * @brief Семибитный I2C-адрес микросхемы.
     */
    uint8_t address_;

    /**
     * @brief Код диапазона входного усилителя.
     */
    uint8_t gain_;

    /**
     * @brief Код режима работы преобразователя.
     */
    uint8_t mode_;

    /**
     * @brief Код скорости преобразования.
     */
    uint8_t dataRate_;

    /**
     * @brief Формирует значение конфигурационного регистра для одиночного преобразования.
     *
     * @param channel Номер single-ended канала: AIN0...AIN3.
     *
     * @return Значение регистра конфигурации ADS1115.
     */
    uint16_t buildSingleShotConfig(uint8_t channel) const;

    /**
     * @brief Записывает шестнадцатиразрядный регистр ADS1115.
     *
     * @param reg Адрес регистра.
     * @param value Записываемое значение.
     *
     * @return true — запись выполнена успешно; false — ошибка обмена.
     */
    bool writeRegister(uint8_t reg, uint16_t value);

    /**
     * @brief Читает шестнадцатиразрядный регистр ADS1115.
     *
     * @param reg Адрес регистра.
     * @param value Указатель для сохранения прочитанного значения.
     *
     * @return true — чтение выполнено успешно; false — ошибка обмена.
     */
    bool readRegister(uint8_t reg, uint16_t* value);
};