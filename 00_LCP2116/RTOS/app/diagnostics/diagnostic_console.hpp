/**
 * @file diagnostic_console.hpp
 * @brief Неблокирующая USB service console базовой прошивки LCP.
 *
 * Console вызывается из единственной прикладной FreeRTOS-задачи. Обработчики
 * команд не должны ожидать периферию в busy-wait циклах: длительная операция
 * запускается командой, хранит состояние в своей подсистеме и завершается через
 * соответствующую `*_poll()` функцию.
 *
 * Точки расширения команды описаны в `diagnostic_console.cpp`. Форматирование
 * новых отчётов выполняется через `diagnostic_output.hpp`.
 */

#pragma once

/**
 * @brief Сбрасывает буфер ввода и состояние периодического отчёта.
 *
 * USB CDC инициализируется прикладным setup() до вызова этой функции.
 */
void diagnostic_console_init(void);

/**
 * @brief Обрабатывает уже принятые USB-байты и плановый periodic report.
 *
 * Функция не ожидает новые данные. Цикл чтения ограничен текущим содержимым RX
 * buffer, поэтому вызов должен выполняться на каждом проходе основной task.
 */
void diagnostic_console_poll(void);
