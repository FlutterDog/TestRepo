/**
 * @file sam3x_tick.hpp
 * @brief Системное время проекта на базе системного тика FreeRTOS.
 */

#pragma once

#include <stdint.h>

/**
 * @brief Обновляет значение SystemCoreClock.
 *
 * Настройку периферии SysTick выполняет порт FreeRTOS при запуске
 * планировщика. Функция сохранена как часть HAL-интерфейса проекта.
 */
void hal_tick_init(void);

/**
 * @brief Возвращает время с момента запуска планировщика в миллисекундах.
 */
uint32_t hal_millis(void);

/**
 * @brief Возвращает время с момента запуска планировщика в микросекундах.
 *
 * Младшая часть значения вычисляется по текущему состоянию счётчика SysTick.
 */
uint32_t hal_micros(void);

/**
 * @brief Выполняет блокирующую задержку в миллисекундах.
 */
void hal_delay_ms(uint32_t ms);

/**
 * @brief Выполняет блокирующую задержку в микросекундах.
 */
void hal_delay_us(uint32_t us);
