#pragma once

#include "project.hpp"

#include <stdint.h>

/**
 * @file lai_board.hpp
 * @brief Аппаратный слой платы LAI1118 rev2.
 *
 * @details
 * Модуль содержит описание аппаратных линий платы и функции доступа к
 * низкоуровневым ресурсам, которые используются прикладным уровнем и
 * протоколом обмена.
 *
 * Карта сигналов:
 *
 * | Порт МК | Сигнал | Назначение |
 * |---|---|---|
 * | PD2 | RS485_DE | Управление направлением RS-485-трансивера |
 * | PD3 | LED_SS | Выбор SPI-регистра светодиодов |
 * | PD4 | MODE_SS | Выбор SPI-регистра режимов аналоговых входов |
 * | PD5 | SHIFT_EN | Разрешение сдвиговых регистров |
 * | PD6 | ADDR0 | Адресный переключатель, бит 0 |
 * | PD7 | ADDR1 | Адресный переключатель, бит 1 |
 * | PB0 | ADDR2 | Адресный переключатель, бит 2 |
 * | PB1 | ADDR3 | Адресный переключатель, бит 3 |
 * | PB2 | ADDR4 / SPI_SS | Адресный переключатель, бит 4; линия SPI SS |
 * | PB3 | SPI_MOSI | SPI MOSI |
 * | PB4 | SPI_MISO | SPI MISO |
 * | PB5 | SPI_SCK | SPI SCK |
 * | PC0 | STATUS_LED | Светодиод состояния связи |
 * | PC4 | I2C_SDA | Линия данных I2C |
 * | PC5 | I2C_SCL | Линия тактирования I2C |
 *
 * @warning
 * Линия PB2 используется на этапе старта как вход адресного переключателя,
 * а после инициализации SPI переводится в выходной уровень HIGH для
 * сохранения режима SPI master на ATmega328P. Поэтому аппаратный адрес
 * должен считываться до инициализации SPI.
 */

/**
 * @namespace lai_board
 * @brief Функции аппаратного доступа платы LAI1118 rev2.
 */
namespace lai_board
{
    /**
     * @brief Инициализирует линии GPIO платы LAI.
     *
     * @details
     * Функция настраивает линии RS-485, выбор SPI-регистров, разрешение
     * сдвиговых регистров, адресные входы с внутренними подтяжками и
     * светодиод состояния связи.
     */
    void initIo(void);

    /**
     * @brief Считывает аппаратный адрес платы.
     *
     * @details
     * Адрес формируется по пяти адресным входам ADDR0...ADDR4.
     * Вход с низким уровнем считается установленным битом адреса.
     *
     * @return Значение аппаратного адреса в диапазоне 0...31.
     */
    uint8_t readSlaveAddress(void);

    /**
     * @brief Устанавливает состояние светодиода связи.
     *
     * @param state true — включить светодиод; false — выключить.
     */
    void statusLedWrite(bool state);

    /**
     * @brief Переключает состояние светодиода связи.
     */
    void statusLedToggle(void);

    /**
     * @brief Переводит RS-485-трансивер в режим передачи.
     */
    void rs485TransmitMode(void);

    /**
     * @brief Переводит RS-485-трансивер в режим приёма.
     */
    void rs485ReceiveMode(void);

    /**
     * @brief Возвращает текущий режим RS-485-трансивера.
     *
     * @return true — включён режим передачи; false — включён режим приёма.
     */
    bool rs485IsTransmitMode(void);

    /**
     * @brief Управляет линией разрешения сдвиговых регистров.
     *
     * @param enabled true — установить высокий уровень; false — установить низкий уровень.
     */
    void setShiftEnable(bool enabled);

    /**
     * @brief Записывает байт в SPI-регистр светодиодов.
     *
     * @param value Значение, передаваемое в регистр светодиодов.
     */
    void writeLedRegister(uint8_t value);

    /**
     * @brief Записывает байт в SPI-регистр режимов аналоговых входов.
     *
     * @param value Маска режимов аналоговых входов.
     */
    void writeModeRegister(uint8_t value);
}